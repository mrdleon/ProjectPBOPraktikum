    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
          try {

            Date tanggalRental =
                    dcTanggalRental.getDate();

            Date tanggalKembali =
                    dcTanggalKembali.getDate();

            if(tanggalRental == null
                    || tanggalKembali == null) {

                javax.swing.JOptionPane.showMessageDialog(
                        this,
                        "Tanggal belum dipilih!"
                );

                return;
            }

            if(tanggalKembali.before(tanggalRental)
                    || tanggalKembali.equals(tanggalRental)) {

                javax.swing.JOptionPane.showMessageDialog(
                        this,
                        "Tanggal kembali tidak valid!"
                );

                return;
            }

            long selisih =
                    tanggalKembali.getTime()
                    - tanggalRental.getTime();

            int lamaHari =
                    (int) TimeUnit.DAYS.convert(
                            selisih,
                            TimeUnit.MILLISECONDS
                    );

            txtHari.setText(
                    String.valueOf(lamaHari)
            );

            double total =
            controller.hitungTotalBiayaPromo(
                    cbKendaraan.getSelectedItem().toString(),
                    lamaHari
            );

            txtTotal.setText(
                    String.valueOf(total)
            );

        } catch(Exception e) {

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    e.getMessage()
            );

        }

    }//GEN-LAST:event_btnHitungActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if(dcTanggalRental.getDate() == null
            || dcTanggalKembali.getDate() == null
            || txtHari.getText().isEmpty()
            || txtTotal.getText().isEmpty()) {

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Data rental belum lengkap!"
            );

            return;
        }

        try {

            controller.simpanRental(
                    cbCustomer.getSelectedItem().toString(),
                    cbKendaraan.getSelectedItem().toString(),
                    dcTanggalRental.getDate(),
                    dcTanggalKembali.getDate(),
                    Integer.parseInt(txtHari.getText()),
                    Double.parseDouble(txtTotal.getText())
            );

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Rental berhasil disimpan!"
            );

            tampilData();

            tampilKendaraan();

            resetForm();

        } catch(Exception e) {

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    e.getMessage()
            );

        }
    }//GEN-LAST:event_btnSimpanActionPerformed

