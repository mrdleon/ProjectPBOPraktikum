    public void tampilData() {

        DefaultTableModel model =
            new DefaultTableModel();

        model.addColumn("ID");
        model.addColumn("Customer");
        model.addColumn("Kendaraan");
        model.addColumn("Tanggal Rental");
        model.addColumn("Tanggal Kembali");
        model.addColumn("Total");

        for(var rental :
                controller.getAllRental()) {

            model.addRow(new Object[] {

                rental.getIdRental(),
                rental.getCustomer().getNamaCustomer(),
                rental.getKendaraan().getNamaKendaraan(),
                rental.getTanggalRental(),
                rental.getTanggalKembali(),
                rental.getTotalBiaya()

            });

        }

        tblRental.setModel(model);

    }
    
    public void resetForm() {

        txtHari.setText("");
        txtTotal.setText("");

        dcTanggalRental.setDate(null);
        dcTanggalKembali.setDate(null);
        
        cbCustomer.setSelectedIndex(0);
        cbKendaraan.setSelectedIndex(0);

    }
    
    public void tampilCustomer() {

        cbCustomer.removeAllItems();

        for(String customer :
                controller.getAllCustomer()) {

            cbCustomer.addItem(customer);

        }

    }
    public void tampilKendaraan() {

        cbKendaraan.removeAllItems();

        for(String kendaraan :
                controller.getKendaraanTersedia()) {

            cbKendaraan.addItem(kendaraan);

        }

    }

