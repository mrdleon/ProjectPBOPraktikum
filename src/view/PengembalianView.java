    public void tampilData() {

         DefaultTableModel model =
            new DefaultTableModel() {

            @Override
            public boolean isCellEditable(
                    int row,
                    int column
            ) {

                return false;

            }

        };

        model.addColumn("ID");
        model.addColumn("Customer");
        model.addColumn("Kendaraan");
        model.addColumn("Tanggal Pengembalian");
        model.addColumn("Denda");

        for(Object[] row
                : controller.getAllPengembalian()) {

            model.addRow(row);

        }

        tblPengembalian.setModel(model);

    }
    
    public void tampilRental() {

         cbRental.removeAllItems();

        for(Integer idRental
                : controller.getRentalAktif()) {

            cbRental.addItem(
                    String.valueOf(idRental)
            );

        }

        if(cbRental.getItemCount() == 0) {

            JOptionPane.showMessageDialog(
                    this,
                    "Tidak ada rental aktif!"
            );

        }

    }

    public void tampilDetailRental() {

        try {

            if(cbRental.getSelectedItem() == null) {

                return;

            }

            Rental rental =
                    controller.getDetailRental(
                            Integer.parseInt(
                                    cbRental
                                            .getSelectedItem()
                                            .toString()
                            )
                    );

            if(rental != null) {

                txtCustomer.setText(
                        rental.getCustomer()
                                .getNamaCustomer()
                );

                txtKendaraan.setText(
                        rental.getKendaraan()
                                .getNamaKendaraan()
                );

                txtTanggalKembali.setText(
                        rental.getTanggalKembali()
                                .toString()
                );

            } else {

                resetForm();

}

        } catch(Exception e) {

            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage()
            );

        }


    }

