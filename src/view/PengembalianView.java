    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
          try {

            if(dcPengembalian.getDate() == null) {

                JOptionPane.showMessageDialog(
                        this,
                        "Tanggal pengembalian belum dipilih!"
                );

                return;
            }

            Date tanggalAsli =
                    java.sql.Date.valueOf(
                            txtTanggalKembali.getText()
                    );

            Date tanggalAktual =
                    dcPengembalian.getDate();

            double denda =
                    controller.hitungDenda(
                            tanggalAsli,
                            tanggalAktual
                    );

            txtDenda.setText(
                    String.format("%.0f", denda)
            );
        } catch(Exception e) {

            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage()
            );

        }
    }//GEN-LAST:event_btnHitungActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
         if(cbRental.getSelectedItem() == null) {

            JOptionPane.showMessageDialog(
                    this,
                    "Tidak ada rental yang dipilih!"
            );

            return;
        }

        if(dcPengembalian.getDate() == null
                || txtDenda.getText().isEmpty()) {

            JOptionPane.showMessageDialog(
                    this,
                    "Hitung denda terlebih dahulu!"
            );

            return;
        }

        try {

            controller.simpanPengembalian(
                    Integer.parseInt(
                            cbRental.getSelectedItem().toString()
                    ),
                    java.sql.Date.valueOf(
                            txtTanggalKembali.getText()
                    ),
                    dcPengembalian.getDate(),
                    Double.parseDouble(
                            txtDenda.getText()
                    ),
                    txtKendaraan.getText()
            );

            JOptionPane.showMessageDialog(
                    this,
                    "Pengembalian berhasil disimpan!"
            );

            tampilData();

            tampilRental();

            txtDenda.setText("");

            dcPengembalian.setDate(null);

            tampilDetailRental();

        } catch(Exception e) {

            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage()
            );

        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    public void resetForm() {

        txtCustomer.setText("");
        txtKendaraan.setText("");
        txtTanggalKembali.setText("");
        txtDenda.setText("");

        dcPengembalian.setDate(null);
    }
    
