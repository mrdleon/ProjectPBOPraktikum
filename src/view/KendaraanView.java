    public void tampilData() {

       DefaultTableModel model =
        new DefaultTableModel() {

            @Override
            public boolean isCellEditable(
                    int row,
                    int column
            ) {

                return false;

            }

        };

        model.addColumn("ID");
        model.addColumn("Nama");
        model.addColumn("Jenis");
        model.addColumn("Plat");
        model.addColumn("Harga");
        model.addColumn("Status");

        try {

            for(var kendaraan
                    : controller.getAllKendaraan()) {

                model.addRow(new Object[]{

                    kendaraan.getIdKendaraan(),
                    kendaraan.getNamaKendaraan(),
                    kendaraan.getClass().getSimpleName(),
                    kendaraan.getPlatNomor(),
                    kendaraan.getHargaSewa(),
                    kendaraan.getStatusKendaraan()

                });

            }

            tblKendaraan.setModel(model);

        } catch(Exception e) {

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    e.getMessage()
            );

        }

    }
    
