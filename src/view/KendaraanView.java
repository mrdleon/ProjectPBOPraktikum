    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if(txtNama.getText().isEmpty()
            || txtPlat.getText().isEmpty()
            || txtHarga.getText().isEmpty()
            || txtStatus.getText().isEmpty()) {

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Semua data harus diisi!"
            );

            return;
        }

        try {

            double harga =
                    Double.parseDouble(
                            txtHarga.getText()
                    );

            controller.simpanKendaraan(
                    txtNama.getText(),
                    cbJenis.getSelectedItem().toString(),
                    txtPlat.getText(),
                    harga,
                    txtStatus.getText()
            );

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Data kendaraan berhasil disimpan!"
            );

            tampilData();

            resetForm();

        } catch(NumberFormatException e) {

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Harga harus berupa angka!"
            );

        } catch(Exception e) {

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    e.getMessage()
            );

        }
        
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void tblKendaraanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKendaraanMouseClicked
        int baris =
        tblKendaraan.getSelectedRow();

        idKendaraan = Integer.parseInt(
                tblKendaraan.getValueAt(baris, 0).toString()
        );

        txtNama.setText(
                tblKendaraan.getValueAt(baris, 1).toString()
        );

        cbJenis.setSelectedItem(
                tblKendaraan.getValueAt(baris, 2).toString()
        );

        txtPlat.setText(
                tblKendaraan.getValueAt(baris, 3).toString()
        );

        txtHarga.setText(
                tblKendaraan.getValueAt(baris, 4).toString()
        );

        txtStatus.setText(
                tblKendaraan.getValueAt(baris, 5).toString()
        );
    }//GEN-LAST:event_tblKendaraanMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(idKendaraan == 0) {

        javax.swing.JOptionPane.showMessageDialog(
                this,
                "Pilih data terlebih dahulu!"
        );

        return;
    }

    if(txtNama.getText().isEmpty()
            || txtPlat.getText().isEmpty()
            || txtHarga.getText().isEmpty()
            || txtStatus.getText().isEmpty()) {

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Semua data harus diisi!"
            );

            return;
        }

        try {

            double harga =
                    Double.parseDouble(
                            txtHarga.getText()
                    );

            controller.updateKendaraan(
                    idKendaraan,
                    txtNama.getText(),
                    cbJenis.getSelectedItem().toString(),
                    txtPlat.getText(),
                    harga,
                    txtStatus.getText()
            );

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Data kendaraan berhasil diupdate!"
            );

            tampilData();

            resetForm();

        } catch(NumberFormatException e) {

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Harga harus berupa angka!"
            );

        } catch(Exception e) {

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    e.getMessage()
            );

        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
         if(idKendaraan == 0) {

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Pilih data terlebih dahulu!"
            );

            return;
        }

        int konfirmasi =
                javax.swing.JOptionPane.showConfirmDialog(
                        this,
                        "Yakin ingin menghapus data?",
                        "Konfirmasi",
                        javax.swing.JOptionPane.YES_NO_OPTION
                );

        if(konfirmasi != javax.swing.JOptionPane.YES_OPTION) {

            return;

        }

        try {

            controller.hapusKendaraan(idKendaraan);

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Data kendaraan berhasil dihapus!"
            );

            tampilData();

            resetForm();

        } catch(Exception e) {

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    e.getMessage()
            );

        }

    }//GEN-LAST:event_btnHapusActionPerformed

